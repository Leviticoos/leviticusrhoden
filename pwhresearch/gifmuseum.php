<h2 id="newbath">
The New Faria Fluid Bath
</h2>
<h5>
<a href="#top">Back to Top</a>
</h5>
<p>
These gifs show off an oil bath model from a paper by Luiz Faria. I reprogrammed this model in order to more accurately simulate the oil droplet that will eventually bounce on top of it. At first, I used a finite element method, meaning that to get derivatives at a point, it looks at its neighboring points. This is less accurate than a method I'll use later, but it is still cool to see here. If you have taken calculus, it is the same method as just taking the slope between two close points to estimate the derivative.
</p><br><br>
<img src="https://media.giphy.com/media/SvkDwgC81BrVzodLYZ/giphy.gif" width="560px" class="center">
<figcaption>
Figure 1, a 3D surface gif of a normal disturbance on a vibrating fluid bath. It's a wave on the surface of a bath of oil vibrating about 80 times a second.
</figcaption><br><br>
<img src="https://media.giphy.com/media/3wMGdb8ajAoh0xQJMj/giphy.gif" width="560px" class="center">
<figcaption>
Figure 2, a cutaway gif of the same normal disturbance on a vibrating fluid bath. The oscillation of the wave is at half the frequency of the oil bath's oscillation, or 40 hertz. This is the same as we see in the real world, meaning the model is not totally off base. Notice the stable "hill" in the wave field, and the oscillating, smaller wave that goes over it.
</figcaption><br><br>
<img src="https://media.giphy.com/media/xFbDUzvF7XRC0w0Wco/giphy.gif" width="560px" class="center">
<figcaption>
Figure 3, a cutaway gif of the same normal disturbance on a vibrating fluid bath, but the oil is shaking more forcefully. It is actually above the <em>faraday amplitude</em> of the bath, meaning that the bath does not trend to no waves, and disturbances are actually amplified over time. You can see that over time the peak of this wave gets higher and higher each oscillation.
</figcaption><br><br>
<img src="https://media.giphy.com/media/r00duTQbgQwO40Dw94/giphy.gif" width="560px" class="center">
<figcaption>
Figure 4, This oil bath has a droplet bouncing on top of it at 40 hertz; that is every time the wave oscillates once, it has a downward point force applied at the center. This causes some errors, as the wave is not smooth after an impact, but pretty soon it converges back into a wave form. In reality, the wave is not this "spikey," this is an error introduced from the inherent pixelated nature of the simulation.
</figcaption><br><br>
<p>
While the finite element method had good results, we can make a more accurate and quicker running simulation using something called <em>fast Fourier transforms</em>, which if you're curious, involves approximating the surface waves as a sum of sine waves, and then taking the derivative of each sine wave individually to find the derivative of the surface wave. This means instead of seeming pixelated, we instead tend to see a small wave pattern in what would be a straight line. But generally, the amplitude of this is small enough that it is not too big a problem.</p><br><br>
</p><br><br>
<img src="https://media.giphy.com/media/jLXbNezPKCNeScJ5OH/giphy.gif" width="560px" class="center">
<figcaption>
Figure 5, Point disturbance on a fft simulated oil bath.
