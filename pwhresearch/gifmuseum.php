<h2 id="phenomena">
Faria Barrier Phenomena
</h2>
<h5>
<a href="#top">Back to Top</a>
</h5>
<p>
Below in <em>The New Faria Fluid Bath</em>, I showed the new vibrating oil bath simulation and how pleased I am with its results. Here, I'll explore how the waves on the surface interact with barriers. All these test start with a very tight, normal disturbance centered on the little blue dot in the graphs. The dashed line is the center of the graph, where x = 0, and the solid lines are where the boundaries of the well are. Inside the bold lines, we are simulating a deep bath of oil (~6mm) that easily allows waves on its surface. Outside them, we have shallow regions that act as a barrier that, in physical experiments, tends to dampen waves.
</p><p>
I explored many different depths for the shallow region, ranging from 0.42mm to 0.000001mm, as zero causes a divide by zero error. While these later tests are not physically possible (the waves are taller and would thus bottom out), I wanted to push the limits to see what would happen, and if I can find a pattern for how the barrier depth effects the wave. I also did tests where the initial drop was at 10mm, 9mm, and 8mm. The 10mm test was right at the boundary, so we should get to see a side by side of dampened and undampened waves. Both 9mm and 8mm are to see how distance from the barrier effects the phenomena observed. First, we look at the 0.42mm deep barrier.
</p><br><br>
<img src="https://media.giphy.com/media/N6uKsLesD7jbsu3lH0/giphy.gif" width="560px" class="center"><br>
<img src="https://media.giphy.com/media/iHEB4h3FCOv6227xNp/giphy.gif" width="560px" class="center"><br>
<img src="https://media.giphy.com/media/PtCS9epMvATRVlrdho/giphy.gif" width="560px" class="center"><br><br>
<p>
These results already are quite cool! While graphically, the difference is small, those small differences may have huge implications once I program the droplet. The underlying wave seems to not mind the barrier much, the island in the middle and the two troughs that move away from the origin. But the small, oscillating waves seem to be dampened significantly once over the border. To show this effect more drastically, let's see the animations where the shallow depth is only 0.01mm.
</p><br><br>
<img src="https://media.giphy.com/media/u3tqN9geEi9u9TpIBt/giphy.gif" width="560px" class="center"><br>
