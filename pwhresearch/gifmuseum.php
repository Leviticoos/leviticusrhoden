These are so cool! We see a much more dramatic dampening of the small oscillating waves, which I think is a super cool thing to note. Maybe it'll have large effects in the bouncing, but I'll be on the lookout for it. I believe the paper says it's a reflection of waves at the barrier that helps making a droplet reflect, but I couldn't quite see any reflection. 
