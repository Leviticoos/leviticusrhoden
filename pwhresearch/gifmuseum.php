<h2 id="tunneling">
Quantum Tunneling
</h2>
<h5>
<a href="#top">Back to Top</a>
</h5>
<p>
This graph takes quite a lot of explanation, and it is honestly a bit out of date. Using the older model of Pilot Wave Hydrodynamics I worked on in early 2021, I ran 10,000 experiments of a droplet walking towards a barrier, set up so it had an approximately 50% chance of reflecting and 50% of tunneling. I took the positions of these 10,000 drops and made an animated histogram so that each black bar represents how many of the 10,000 droplets were at that place at that time. What we end up with is a probability distribution over time. The higher the bar at a point, the more likely you are to find a random droplet there. Now, if you are familiar with quantum stuffs, you'll recognize this is similar to the square of the wavefunction of position! In fact, this graph looks quite similar to quantum tunneling graphs! I hope to recreate it with the Faria model soon.
</p><br><br>
