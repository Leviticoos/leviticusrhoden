<h2 id="distillingphi">
Distilling φ 
</h2>
<h5>
<a href="#top">Back to Top</a>
</h5>
<p>
Alright, here we have some quite pretty graphs. If you haven't read <em>Distilling η</em> bellow, then I'll fill ya in quick. Each of the PDEs in this model consist of a sum of a few terms, 2 in the case of η and 3 in the case of φ. Bellow, we 'distilled' η by looking at the parts of its sum over time, and here we shall do that with φ. Below you will find an animation for all of these parts, and the sum of them with the black line.
</p><br><br>
<img src="https://media.giphy.com/media/OEgChlQWZkzEtRh6Kj/giphy.gif" width="560px" class="center"><br>
<br><br>
<p>
This gif is an absolutely wild unit of a time, wouldn't you agree? I can't tell what is going on at all, so let's split it up! First, we look at the green line compared with the black line sum. In the PDE, green is a term involving the shaking of the table and the wavefeild η. Since this model reframes the shaking table as simply changing gravity, I'll call this the gravity-η component of φ. It's somewhwat easy to remember, green for gravity!
</p><br><br>
<img src="https://media.giphy.com/media/mJzxixU5xGVTpxBiPQ/giphy.gif" width="560px" class="center"><br>
<br><br>
<p>
Let's break down what we see here. The green wave is essentially a Bessel function, with its midline hovering around the black line. That is to say, it is a Bessel function that is shifted upward to correspond to the height of black line φ. Ignoring the initial spread of the Bessel, it is consistent in shape, and it looks like amplitude is consistent as well (although citation needed here), and changes mostly in that midline offset. The midline offset does appear to ignore the oscillations in the black line and center instead on the long form waves in the black line, though more thorough testing would be needed to conclude that for sure.
</p><p>
Next is the blue line, which is based on the double spatial derivative of η. Since η is the fluid bath, I made it blue. It seems to be almost a Bessel function too, although it has an odd bit in its center trough, almost like a chunk was cut out. It seems to keep its midline at the zero point, but varies its amplitude over time.
</p><br><br>
<img src="https://media.giphy.com/media/DAyoEbQ4FfnwjKL7nW/giphy.gif" width="560px" class="center"><br>
<br><br>
<p>
And now onto the red line, which is the double derivative of φ. No, it doesn't have as fun a reason for its color.
</p><br><br>
<img src="https://media.giphy.com/media/P8YwhAtK6Wc1rlyw6a/giphy.gif" width="560px" class="center"><br>
<br><br>
<p>
Now, if you're me, this might seem oddly like the missing bit of the blue bessel function! It seems to pretty much only peak at the center, while the blue wave is missing this! Since this distilling process is 100% following hunches, I decided to graph the sum of these two waves, aptly colored "purple". Ok, it came out magenta. Sue me!
</p><br><br>
<img src="https://media.giphy.com/media/rYzMzbn1ItKuPh20sy/giphy.gif" width="560px" class="center"><br>
<br><br>
<p>
Oh hoh hoh, yeah. We're getting somewhere! Unless this results in disappointment in the end. But a man can dream. This wave seems to be be responsible for the small oscillations in the black line, as they correlate with the increasing and decreasing amplitude of the purple/magenta line. Where peaks are, the black line peaks when amplitude is max and troughs when it is min, and vice versa for the troughs. To really see how these two interact, let's graph the purple/magenta line and the green line together.
</p><br><br>
<img src="https://media.giphy.com/media/EpEuJ7bfKKOFg7wuwv/giphy.gif" width="560px" class="center"><br>
<br><br>
<p>
Absolute poetry! The troughs and peaks of the two colors line up perfectly, cancelling out mostly except for when magenta modulates in amplitude, and following the center of green. This seems to imply that if we set green's midline to zero, we would have no long form, non-oscillating waves, and thus it acounts for that. Likewise, that amplitude modulation in the purple wave accounts mostly for the small oscillating waves! If these can be sorted out and described with time-dependent equations, we may be golden, at least when solving for φ.
