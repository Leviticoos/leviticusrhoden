<br><br>
<p>
    When the base and dish assembly are rotated relative to each other, the triangles on the twist base fingers act like ramps, that allow counterclockwise rotation but not clockwise rotation of the dish. When the dish is turned counterclockwise, a spring force is generated by the deflection of the fingers that holds the dish on tight, while a clockwise rotation will go until the fingers hit the side of the slots in the speaker adapter, making it easy to remove without seeing the underside of the dish.
</p><br><br>

<h2>Conclusion</h2>
<p>
    All in all, I'm impressed with how well this project turned out! This was my first project with my new 3D printer, and I missed how quickly the prototyping process was. In fact, I have a picture that shows well just how many different measurements I tried. 
</p>
<img src="photos/base_progresion.jpg" alt="A CAD assembly of all the above parts, put together." style="display: block; margin-left: auto; margin-right: auto;">
<figcaption>If at first you don't succeed, try, try, try, try again!</figcaption>
<br><p>
    I'm impressed with how well it came together. To deal with some tolerance inconsistancies with 3D printers I place a few pieces of electrical tape on the bottom of the dish. While each print I keep tweaking the numbers a bit to try and get a better fit, this lets me have a well fitting part regardless of how my printer is feeling day to day.
</p>
