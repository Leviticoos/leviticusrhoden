<h2>
Interchangeable 3D Printed Dishes
</h2>
<p>
For the experiments I wanted to run, I needed a way to make different shaped dishes, with a certain topology layout of deep and shallow oil. That lets us make corrals that trap droplets, narrow channels for them to pass through, etc. Starting this mission, I had to design a low force, yet sturdy connection. We have to connect our dishes to a thin metal cylinder that houses the coil in the speaker. I figured I could use the slight bend that thin plastic allows to make a tight fit. 
</p>
<p>
    After some CADing, I got the following models worked up. Since 3D printing is limited in how it makes overhangs, and I didn't want to waste time with supports, so I made the interchangeable dishes two parts. One is the dish itself, with 3 insert holes on the bottom. Then I have the twist collar with 3 posts. This also allowed for easier prototyping, as I could hone the twist part independently of the dishes. On the speaker is also a disk with three slots to mount the dishes on.
</p>
