<h2>
What is an Oil Bath Shaker?
</h2>
<p>
As explained in <a href="whatispwh.php">my introduction to Pilot Wave Hydrodynamics</a> (PWH), the model is just a shaking bath of oil. So, we build a machine to shake a dish of oil. In my case it is a speaker on top of some wood posts that I can feed sine waves from my iPhone. This is much in line with other experiments in PWH, at least the lower-cost ones. Of course, one could spend as much as they want to get industry-grade shakers that simulate earthquakes and whatnot, but my simple shaker is more than enough to test some of the phenomena. 
</p><br><br>

<h2>
What is My Oil Bath Shaker?
</h2>
<p>
I built my shaker using mostly parts I had at home. While it is for research at the university, it started as a project I did first for a physics competition, and later for fun. Four pine posts were cut from some scrap I had lying around on my bandsaw, and I cut slots in the lower part of them to hold a plywood shelf. This shelf both holds the posts together and the amplifier circuit that runs the speaker. On the top of posts, I screwed in the speaker. This, while not as professional as I had hoped, does give me some leeway in leveling out the speakers, by tightening the screws fully or leaving them a few millimeters above the posts.
</p>
