<h2>
Introduction
</h2>
<p>
As mentioned on the P.W.H. Research page, these equations are based off of <em>A model for Faraday pilot waves over variable topography</em> by Luiz Faria. This work is one of the main ones I'm basing my research off of. This is meant to be a write-up of sorts about what these equations are, what each term means, how I tried breaking them up to better understand them, and possibly to find a better, faster way to simulate them.
</p><br><br>
  
