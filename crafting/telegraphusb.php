<h2>
Introduction
</h2>
<p>
The <a href="">Morse Telegraph Club (MTC)</a> has put together both an <a href="https://sites.google.com/site/morsekob/home">internet connected telegraph network</a> and a <a href="https://sites.google.com/site/morsekob/turnkey-interface/connecting-the-interface">telegraph to USB converter</a>! This allows your old telegraph equipment to send and recive messages across the globe on a simulated loop! It has been a few years since these were made, at the time of my working on it sometime in the first half of the teens. And, as luck would have it, I've been looking for an excuse to learn PCB designing!
</p><p>
The goal of this project is to turn <a href="https://sites.google.com/site/morsekob/turnkey-interface/schematic-and-layout">this schematic</a> into an actionable product. Update it to be made with quick PCB prototyping, usb C possibly, and start tapping away on MorseKOB. It will also be a great chance to make a BOM, figure out which parts to get and how bulk buying effects that.
</p><br><br>

