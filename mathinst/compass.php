<h2>
Bion's Construction
</h2>
<blockquote>
<p>There are ſeveral Sorts of Compaſſes, of which we ſhall ſpeak more fully hereafter; but that whoſe Uſes we intend to lay down in this Chapter, is the common Pair of Compaſſes. Of theſe Compaſſes there are two kinds, viz. Simple ones, which have their Points fixed, and others whoſe Points may be taken off; both kinds being of different Bigneſſes, but they are commonly in Length from three to ſix Inches. To theſe Compaſſes, that ſhift their Points, there belongs a Drawing-Pen-Point, a Pencil-Point, and ſometimes a Dotting-Wheel, to make dotted Lines.
</p><p>
The Goodneſs of compaſſes conſiſts chiefly in this, That the Motion of their Head be very equable, that ſo they may not leap in opening and ſhutting; that the Joints be well fitted; that they are well filed and poliſhed; and , laſtly, that the Steel-Points are well joined and equal. The Figure A ſheweth theſe kinds of Compaſſes, whoſse Conſtruction we ſhall give in the third Book.
</p><p>
    <b>Book I, Chapter I</b>
</p></blockquote>
