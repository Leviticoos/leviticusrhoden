<h2>
My Process
</h2>
<p>
How you develop film photos is kind of like politics, or working out. Everyone has an opinion and everyone thinks they're right. And thats fine I guess, the goal of this is not to change your ways or convince you I'm right. This is mostly my notes so I can continue to improve my developing methods. All that said, a lot of my inspiration is from <a href='https://www.youtube.com/watch?v=WpgsITqoDXQ&t=945s'>Alex from TechnologyConnections</a>, and I've been winging it off of that since. Right below here is my writen method, and below that is a little section that goes over lessons learned from my diffrent rolls I've developed. And in case you are curious, the bold notes are things that have sunk past developing.
</p>
<br>
<dt>-Load Film in a dark bag</dt>
<dd>This is the standard procedure, put canister opener, scisorrs, all parts of the dark can (body, lid, and spool), and the film. <b>Make sure to cut off the film lead and make the edge clean.</b> Spool it up and close the dark can, then take it out.</dd>
<br><dt>-Mix one part Rhodinal developer to 100 parts tap water, 4ml and 400ml for one roll</dt>
<dd>The dark can says it only needs 375ml, but that stresses me out. Also, I have not found (yet) these numbers to be too picky, I measure the 4ml in a 10ml graduated cylinder, but I meassure the 400ml directly in a 1 liter meassuring cup, so it has a solid +/-13ml. I have also found that one should <b>prepare the rodinal solution the day off,</b> as the mixed solution seems to expire quickly.</dd>
<br><dt>-Shake gently, stir with the stir stick, bang on the counter gently to knock off any air bubles</dt>
<br><dt>-Let sit 30 minutes</dt>
<br><dt>-Stir and bang on counter again, wait 30 more minutes</dt>
<br><dt>-Pour out developer into sink with running water (to dilute it)</dt>
<br><dt>-Run room temp water into dark can, mixing and shaking for a few minutes</dt>
<dd>This step acts as a stop bath. Since Rodinal stand development takes an hour, an extra minute to stop the developing reaction does not mean too much, so we don't need a dedicated stop bath.</dd>
<br><dt>-Test snipped lead of film and fixer, to ensure fixer solution still works.</dt>
<dd>If the leader does not turn clear within 5 minutes, it is no longer active and you need to safely pour it out and mix a new batch. I made a liter of it, using about half of that volume every roll, and it has not run out of 'fixing' power in 7 rolls. I currently use arista primium oderless fixer, and while it is my only point of reference, it is doing a fine job.</dd>
<br><dt>-Roughly meassure out 400ml of fixer and pour into dark can</dt>
<br><dt>-Mix and stir often(once every minute or 3), remembering to bang lightly to knock off air bubbles. Do this for about 12 minutes.</dt>
<dd>I have not found it possible to 'over fix' a roll, at least not yet. So I always err on the side of leaving it in for 15 minutes or so. But I don't think much more than 10 is actually needed.</dd>
<br><dt>-Reclaim Fixer from dark can</dt>
<br><dt>-Rinse with water as before</dt>
<br><dt>-<b>Prepare surfactant solution/wetting agent</b></dt>
<dd>This step is a water bath with a little bit of something like soap, so the water dosn't bead up and instead easily rolls off of the film. I live in Seattle, so I imagined I could get away without mineral deposits/water spots. No dice, so now I use a surfactant solution as a last step. It's not bad, I think 1 part to 200, so it should last forever.</dd>
<br><dt>-Pour in wetting agent/water solution, then drain</dt>
<br><dt>-Carefully remove film from the spool, then hang to dry <b>for at least an hour</b></dt>
<dd>Yeah, I got to excited, and also made the mistake of setting the strip on my bed. It hadn't dried yet, so a whole layer of threads and dirt got stuck to the emulsion layer. Remeber, shooting and developing film is a long, slow process, and a few hours/an extra night of patience will be well worth it.</dd>

