<h2>Getters</h2> 
<h3>Nov. 1st, 2022</h3>
<p>It looks like we may be able to use just titanium wire for getters, although it may need to be super thin. The idea being you get the Ti hot and then it'll deposit onto the glass. This heating can either be by placing the sealed tube in the oven or by running current through the Ti wire/foil so it heats up as a filament. Either way, it seems making stuff thin is crutial to having it heat up enough and react. Most info from <a href="http://diyvacuumtubes.com/testing-diy-getters-t25.html">this forum post.</a> Simplifier used 0.8mm titanium wire for some tests, and simply make the plate out of titanium for later tests, so it served double duty.</p>
<br><br>

<h2>Glassblowing</h2>
<p>
This part seems easier than I first thought! It falls into a few different catagories below.
</p>
<h3>Hermetic Wire Seal</h3>
<p>
Spot weld nickle to copper wire, then use fine (~200 grit) sandpaper to mirror finish tungston wire. Weld the tungston wire onto the nickle-ized bit of the copper. The tungston will form the seal with the glass, while the copper will be your leads. Then spot weld some heated and sanded steel wire to the other end of the tungston wires, such that they provide a structural connection between the two wires. Then heat up the tungston, as well as the leads to outgas them. Clean the tungston portion with flux remover and again place into flame to remove/evaporate the flux remover. Then clean tungston with dry qtip.
</p> <p>
To seal them in glass, you have a fluted piece of glass that you stick the wires through, then heat up the glass and use pliars to squish it around the wires. Be sure to insulate the glass after this heating cycle, such that it cools down very slowly. You should get a yellowy or gold-ish color of the tungston wire if you got a good seal.
</p>
<br><br>

<h2>Filament, Plate, & Grid</h2>
<p>
This step is pretty fun, and I think the most forgiving part of this project. First things first, while tungston wire is used for filament and hermetic seal purposes, I'll need to use a stronger wire for mechanical reasons except in those specific cases I need tungston. Think of the leads out the bottom, holding filament, plate and grid in place, and the grid itself. Most people seem to use nickle wire for this. 
</p>
<p>
While I've seen some people use a titanium plate for the collector, so it doubles as a getter, but I'm not a fan of that. I'll use a bog standard titanium loop, seperate from the other parts. The two challenges will be; a) how to mount the titanium loop, b) how to heat it up. You can either do an electrical flash (have two extra leads out of the tube, such that you can run enough current through it to heat up the titanium), or use an inductance coil to induce the current in the titanium. The main points are; a) how many leads do you want out the bottom (4 or 6)? b) can the wire supporting the titanium withstand the heat? c) can I borrow and inductance coil? I think the best way for at least the first few tests will be to have a 5th and 6th lead out the bottom, until I get everything else down. This helps too in that I can kick the buying a tool can down the road.
</p>

