<p>
Let’s consider mathematical tools throughout history. Yes the title says computer, but really, what is a computer if not the most advanced mathematical tool we’ve made to date? 
</p><p>
We start with paper. As far as being a calculator goes, it’s not. We have to give it everything. What to calculate, the calculating power, everything. In reality, it’s not a number crunching tool at all! But what if we solved a bunch of problems and wrote those down on paper? We now have a mathematical table. This is surely a mathematical tool! Sure, every value has been calculated by a person, so it's not that impressive. In fact, let’s define this as a level 0 mathematical tool. Each cell in this table can do no real calculation, it solves nothing, it is simply saved information that another person calculated and allows it to be used by a mathematician or engineer. It merely tells you a bunch of answers and hopefully it has the one you need.
</p><p>
Now we consider a slide rule, a level 1 mathematical tool. What makes it so? Well, it is designed to solve a specific equation, namely that of multiplication. Sure, some have fancy log and cosine scales, but the heart of this slide rule is simply the C and D scales. This tool is different from a table in that I can solve for any values that come to my mind. While the tables only help me if someone else has solved my exact problem, like maybe the natural log of two point eight seven, this let’s me solve an equation for any values. The man who designed a given slide rule, instead of solving exact problems, “solved” an equation! I can calculate three point nine two times five hundred and twenty, and even if no one else in the history of humanity has ever calculated that exact product, a slide rule can give it to me. It is pre-programmed only in that it solves a predefined equation, that being A times B equals C.
</p><p>
Computers, then, are level 2. You can input an equation, any equation, and it’ll solve it! Sure, caveats abound with the phrase “any equation,” but some stuff doesn't work with a slide rule, either. The main idea is that a computer is built in a way that I can ask it to calculate the sine of the cosine of the sine of the square root of x factorial, and it can! No one designed the computer to solve for the sine of cosine of…, much like no one designed the slide rule to solve three point nine two times five hundred and twenty. Nonetheless, the computer gives me the answer!
</p><p>
So, what would be a level 3 tool? This, I think, is what we can do and a computer can’t. A computer struggles to make its own equation. A slide rule takes values, a computer takes equations, the next step would be something that can make the equation from an input, be it a problem, or a set of data, or something else. (And no, line of best fit does not count because that line is in fact determined by pre-programed equations.) As far as we’ve tried, and we have made some great progress trying, we can’t make a computer generate equations in any general way. That’s what scientists, businessmen, musicians etc. try to do! They know what a star does while it burns, or what the results of a good investment are, or what musical notes strike the human fancy just right. And it’s these things we as level 3 mathematicians try and do. We want to fit math onto these things we know and see in the world! That is what we can do that computers can’t!
</p><p>
Someday, we will eventually create a general Artificial Intelligence, and have at our hands a level 3 mathematical tool. But for today and the foreseeable future, we can take pride in being the only level 3 mathematicians on earth, and quite possibly the universe. Every bit of math you commit to paper is something that nothing else can create. Nothing short of a human can write an algebraic expression. Even if it’s your first one, even if it is simply y equals x. You can throw apples at a computer all day but it will never write down an equation for gravity. That task falls to us.
</p><br><br>

